export const movieReducer = (state = initialState, action) => {
  switch (action.type) {
    case ADD_MOVIE:
      return state.concat(action.payload);
    case DELETE_MOVIE:
      return state.filter(e => e.id !== action.payload);
    case EDIT_MOVIE:
      return state.map(e =>
        e.id === action.payload.id ? ({ ...e } = { ...action.payload }) : e
      );

    default:
      return state;
  }
};
